      $("#jumbo-header").click(function(e){
          parent = $(this);
          //create .ink element if it doesn't exist
          parent.find(".ink").remove();
          parent.find(".ink-2").remove();

          if(parent.find(".ink").length == 0)
            parent.prepend("<span class='ink'></span>");

          if(parent.find(".ink-2").length == 0)
            parent.prepend("<span class='ink-2'></span>");

          ink = parent.find(".ink");
          ink_2 = parent.find(".ink-2");
          ink_3 = parent.find(".ink-3");
          //incase of quick double clicks stop the previous animation
          ink.removeClass("animate");
          ink_2.removeClass("animate");
          ink_3.removeClass("animate");

          //set size of .ink
          if(!ink.height() && !ink.width())
          {
            //use parent's width or height whichever is larger for the diameter to make a circle which can cover the entire element.
            d = Math.min(parent.outerWidth(), parent.outerHeight());
            ink.css({height: d, width: d});
            ink_2.css({height: d/2, width: d/2});
          }

          //get click coordinates
          //logic = click coordinates relative to page - parent's position relative to page - half of self height/width to make it controllable from the center;
          x = e.pageX - parent.offset().left - ink.width()/2;
          y = e.pageY - parent.offset().top - ink.height()/2;

          x_2 = e.pageX - parent.offset().left - ink_2.width()/2;
          y_2 = e.pageY - parent.offset().top - ink_2.height()/2;
          //set the position and add class .animate
          ink.css({top: y+'px', left: x+'px'}).addClass("animate");
          ink_2.css({top: y_2+'px', left: x_2+'px'}).addClass("animate");
          if(e.pageX < $(window).width()/2) {
            hover_1();
            setTimeout(function() {hover_1();}, 800);
          }
          else {
            hover_2();
            setTimeout(function() {hover_2();}, 800);
          }
        })
